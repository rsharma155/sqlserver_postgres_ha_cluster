CREATE OR ALTER PROCEDURE crud_erp_create_timesheet @employee_id INT, @task_id INT, @hours DECIMAL(5,2) AS
BEGIN SET NOCOUNT ON;
    DECLARE @actual_emp_id INT, @actual_task_id INT;
    SELECT TOP 1 @actual_emp_id = employee_id FROM employees ORDER BY NEWID();
    SELECT TOP 1 @actual_task_id = task_id FROM project_tasks ORDER BY NEWID();
    IF @actual_emp_id IS NULL OR @actual_task_id IS NULL BEGIN SELECT -1 AS timesheet_id; RETURN; END;
    INSERT INTO timesheets (employee_id, task_id, work_date, hours, description)
    VALUES (@actual_emp_id, @actual_task_id, GETDATE(), @hours, 'Load test entry');
    SELECT SCOPE_IDENTITY() AS timesheet_id;
END;
GO

CREATE OR ALTER PROCEDURE crud_erp_create_project @name VARCHAR(200), @budget DECIMAL(14,2) AS
BEGIN SET NOCOUNT ON;
    DECLARE @company_id INT, @project_id INT, @code VARCHAR(50);
    SELECT TOP 1 @company_id = company_id FROM companies ORDER BY NEWID();
    IF @company_id IS NULL BEGIN SELECT -1 AS project_id; RETURN; END;
    
    SET @code = CONCAT('PRJ-', CAST(DATEDIFF(SECOND, '1970-01-01', GETDATE()) AS VARCHAR), '-', CAST(ABS(CHECKSUM(NEWID())) % 100000 AS VARCHAR));
    
    -- Safety check for unique project code
    IF EXISTS (SELECT 1 FROM projects WHERE code = @code)
        SET @code = CONCAT(@code, '-', CAST(ABS(CHECKSUM(NEWID())) % 1000 AS VARCHAR));

    INSERT INTO projects (company_id, name, code, description, budget, status)
    VALUES (@company_id, @name, @code, 'Generated by load test', CAST(@budget AS DECIMAL(14,2)), 'planning');
    SET @project_id = SCOPE_IDENTITY();
    INSERT INTO project_tasks (project_id, name, description, status)
    SELECT @project_id, 'Task ' + CAST(g AS VARCHAR), 'Auto-generated task', 'pending'
    FROM (VALUES(1),(2),(3)) AS nums(g);
    SELECT @project_id AS project_id;
END;
GO

